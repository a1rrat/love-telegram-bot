async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(
        "Привет, любовь ❤️\n\n"
        "Введи 5-значный код с твоего сертификата."
    )


async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE):
    text = update.message.text.strip()

    if not (len(text) == 5 and text.isdigit()):
        await update.message.reply_text(
            "Не узнаю этот код 🤍 Проверь 5 цифр на сертификате."
        )
        return

    if text in CODES:
        congrats, activity = CODES[text]
        await update.message.reply_text(congrats)
        await update.message.reply_text(activity)
    else:
        await update.message.reply_text(
            "Не узнаю этот код 🤍 Проверь 5 цифр на сертификате."
        )


def main():
    app = ApplicationBuilder().token(BOT_TOKEN).build()

    app.add_handler(CommandHandler("start", start))
    app.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, handle_message))

    app.run_polling()


if __name__ == "__main__":
    main()
